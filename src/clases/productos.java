package clases;

public class productos {
	int id_producto;
	String nombre_producto;
	int codigo_producto;
	String descripcion_producto;
	int cantidad_producto;
	double costo_producto;
	double valor_producto;
	double utilidad_producto;
	String fecha_producto;
	String imagen_producto;

	public int getId_producto() {
		return id_producto;
	}

	public void setId_producto(int id_producto) {
		this.id_producto = id_producto;
	}

	public String getNombre_producto() {
		return nombre_producto;
	}

	public void setNombre_producto(String nombre_producto) {
		this.nombre_producto = nombre_producto;
	}

	public int getCodigo_producto() {
		return codigo_producto;
	}

	public void setCodigo_producto(int codigo_producto) {
		this.codigo_producto = codigo_producto;
	}

	public String getDescripcion_producto() {
		return descripcion_producto;
	}

	public void setDescripcion_producto(String descripcion_producto) {
		this.descripcion_producto = descripcion_producto;
	}

	public int getCantidad_producto() {
		return cantidad_producto;
	}

	public void setCantidad_producto(int cantidad_producto) {
		this.cantidad_producto = cantidad_producto;
	}

	public double getCosto_producto() {
		return costo_producto;
	}

	public void setCosto_producto(double costo_producto) {
		this.costo_producto = costo_producto;
	}

	public double getValor_producto() {
		return valor_producto;
	}

	public void setValor_producto(double valor_producto) {
		this.valor_producto = valor_producto;
	}

	public double getUtilidad_producto() {
		return utilidad_producto;
	}

	public void setUtilidad_producto(double utilidad_producto) {
		this.utilidad_producto = utilidad_producto;
	}

	public String getFecha_producto() {
		return fecha_producto;
	}

	public void setFecha_producto(String fecha_producto) {
		this.fecha_producto = fecha_producto;
	}

	public String getImagen_producto() {
		return imagen_producto;
	}

	public void setImagen_producto(String imagen_producto) {
		this.imagen_producto = imagen_producto;
	}

}
