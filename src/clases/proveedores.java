package clases;

public class proveedores {

	int id_proveedor;
	String tipo_proveedor;
	String nombre_proveedor;
	String direccion_proveedor;
	String telefono_proveedor;
	String ps_proveedor;
	String detalle_proveedor;

	public int getId_proveedor() {
		return id_proveedor;
	}

	public void setId_proveedor(int id_proveedor) {
		this.id_proveedor = id_proveedor;
	}

	public String getTipo_proveedor() {
		return tipo_proveedor;
	}

	public void setTipo_proveedor(String tipo_proveedor) {
		this.tipo_proveedor = tipo_proveedor;
	}

	public String getNombre_proveedor() {
		return nombre_proveedor;
	}

	public void setNombre_proveedor(String nombre_proveedor) {
		this.nombre_proveedor = nombre_proveedor;
	}

	public String getDireccion_proveedor() {
		return direccion_proveedor;
	}

	public void setDireccion_proveedor(String direccion_proveedor) {
		this.direccion_proveedor = direccion_proveedor;
	}

	public String getTelefono_proveedor() {
		return telefono_proveedor;
	}

	public void setTelefono_proveedor(String telefono_proveedor) {
		this.telefono_proveedor = telefono_proveedor;
	}

	public String getPs_proveedor() {
		return ps_proveedor;
	}

	public void setPs_proveedor(String ps_proveedor) {
		this.ps_proveedor = ps_proveedor;
	}

	public String getDetalle_proveedor() {
		return detalle_proveedor;
	}

	public void setDetalle_proveedor(String detalle_proveedor) {
		this.detalle_proveedor = detalle_proveedor;
	}

}
